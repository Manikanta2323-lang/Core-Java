package geeksforgeekssolutions;

public class assigmentoperator {
    public static void main(String[] args) {
        int a=7;

        System.out.println("a +=3: "+(a +=3));
        
        System.out.println("a -=3: "+(a -=3));
        System.out.println("a *=3: "+(a *=3));
        System.out.println("a /=3: "+(a /=3));
        System.out.println("a %=3: "+(a %=3));
   
    }

}
